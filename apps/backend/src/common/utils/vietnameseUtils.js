/**
 * Normalize Vietnamese text for search (remove diacritics, lowercase, trim)
 * @param {string} str - Input string
 * @returns {string} Normalized string
 */
export const normalizeVietnamese = (str) => {
  if (!str) return '';
  return removeDiacritics(str).toLowerCase().trim();
};

/**
 * Extract keywords from disease name (supports both with and without diacritics)
 * @param {string} diseaseName - Disease name
 * @returns {string[]} Array of keywords
 */
export const extractDiseaseKeywords = (diseaseName) => {
  if (!diseaseName) return [];
  
  // Remove common words
  let cleaned = diseaseName
    .toLowerCase()
    .replace(/bệnh|disease|gây hại|trên|của|cây|benh|gay hai|tren|cua|cay/gi, '')
    .trim();
  
  // Remove diacritics for better matching
  cleaned = removeDiacritics(cleaned);
  
  // Split by space, comma, or other separators
  const keywords = cleaned
    .split(/[\s,;|]+/)
    .map(k => k.trim())
    .filter(k => k.length > 2); // Only keep words > 2 chars
  
  return keywords;
};

/**
 * Calculate similarity between two strings (simple Levenshtein-like)
 * @param {string} str1 - First string
 * @param {string} str2 - Second string
 * @returns {number} Similarity score (0-1)
 */
export const calculateSimilarity = (str1, str2) => {
  if (!str1 || !str2) return 0;
  
  const s1 = normalizeVietnamese(str1);
  const s2 = normalizeVietnamese(str2);
  
  // Exact match
  if (s1 === s2) return 1;
  
  // Contains match
  if (s1.includes(s2) || s2.includes(s1)) return 0.8;
  
  // Calculate common characters
  const longer = s1.length > s2.length ? s1 : s2;
  const shorter = s1.length > s2.length ? s2 : s1;
  
  let matches = 0;
  for (let i = 0; i < shorter.length; i++) {
    if (longer.includes(shorter[i])) {
      matches++;
    }
  }
  
  return matches / longer.length;
};

